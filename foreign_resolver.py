def _norm_key(value: str | None) -> str:
    text = str(value or "").strip().casefold()
    text = re.sub(r"^(гр|с|кв|жк)\.?\s*", "", text)
    text = re.sub(r"[^\w]+", "", text, flags=re.UNICODE)
    return text


