def resolve_foreign_place(home_country: str, place_name: str | None, place_code: str | None) -> ForeignPlaceMatch | None:
    home = str(home_country or "").strip().upper()
    if len(home) != 2:
        return None

    raw_code = str(place_code or "").strip()
    raw_name = str(place_name or "").strip()
    name_key = _norm_key(raw_name)

    # BG 2009-style foreign section codes.
    if home == "BG" and raw_code.startswith("99") and len(raw_code) >= 7:
        host = _BG_2009_PREFIX_TO_HOST.get(raw_code[:7])
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_by_code_prefix",
            )

    # Country-name rows (e.g., BG 2013 abroad section totals).
    if home == "BG" and name_key:
        host = _BG_COUNTRY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_country_name",
            )

        # City-name fallback.
        host = _BG_CITY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_city_name",
            )
        
    if home == "PT":
        host = _PT_COUNTRY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_country_name",
            )
        
    if home == "HR":
        host = _HR_COUNTRY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_country_name",
            )
        
    if home == "IT":
        host = _IT_COUNTRY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_country_name",
            )
        
    if home == "PL":
        host = _PL_COUNTRY_ALIASES.get(name_key)
        if host:
            return ForeignPlaceMatch(
                host_country=host,
                canonical_code=f"{home}_{home}ZZ{host}",
                canonical_name=_COUNTRY_EN_NAME.get(host, host),
                reason="matched_foreign_country_name",
            )
        # City-name fallback.
        # Normalise before keying: take text before first comma/period, strip
        # trailing Roman numerals or digits (e.g. "Dublin III" → "Dublin").
        city_raw = re.sub(r"[,\.]\s*.*$", "", raw_name).strip()
        city_raw = re.sub(r"\s+[IVXivx\d]+$", "", city_raw).strip()
        city_key = _norm_key(city_raw)
        host = _PL_CITY_ALIASES.get(city_key) or _PL_CITY_ALIASES.get(name_key)
